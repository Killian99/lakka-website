<h2>Create your installation medium</h2>

<p>To setup Lakka on a PC, you have to create a Live USB installer. You need a USB pendrive of at least 512mo. All data on this drive will be completely lost.</p>

<h3>Download Windows image flasher utility</h3>

<p>On Windows, you will need a graphical tool to flash Lakka to your USB pen.</p>

<p>This tool is called Win32DiskImager and is free.</p>

<p class="dl"><a href="http://sourceforge.net/projects/win32diskimager/">Download Win32DiskImager <span class="details">for Windows ~ 17MB</span></a></p>

<h3>Setup Windows image flasher utility</h3>

<p><img src="images/win32diskmanager1.png" /></p>

<h3>Determine your USB drive</h3>

<p>Open your File Manager, and plug your USB pen.</p>

<p>You will see a new drive appearing in your File Manager.</p>

<h3>Flash the image</h3>

<p>Run the <em>Win32DiskImager</em> you just installed.</p>

<p><img src="images/win32diskmanager2.png" /></p>

<p>Select Lakka and the USB pendrive, and hit the <em>Write</em> button.</p>